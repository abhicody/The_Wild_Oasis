export async function updateSetting(formData) {
  const minBookingLength = formData.get("minBookingLength");
  const maxBookingLength = formData.get("maxBookingLength");
  const maxGuestsPerBooking = formData.get("maxGuestsPerBooking");
  const breakfastPrice = formData.get("breakfastPrice");

  const updateData = {
    minBookingLength,
    maxBookingLength,
    maxGuestsPerBooking,
    breakfastPrice,
  };

  const { data, error } = await supabase
    .from("settings")
    .update(updateData)
    // There is only ONE row of settings, and it has the ID=1, and so this is the updated one
    .eq("id", 1)
    .single();

  if (error) {
    console.error(error);
    throw new Error("Settings could not be updated");
  }
  return data;
}

